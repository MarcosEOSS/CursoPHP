<?php
/*Comparadores*/

$a = "7" == 7; //IGUAL
$b = 7 != 7; //DIFERENTE (Pode usar <>)
$c = "7" === 7; //IDENTICO
$d = 7 !== 7; //NAO IDENTICOS
$e = 7 > 8; //Maior
$f = 7 < 8; //Menor;
$g = 7 <= 8; //Menor ou igual
$h = 7 >= 8; //Maior ou igual

var_dump($a);
var_dump($b);
var_dump($c);
var_dump($d);
var_dump($e);
var_dump($f);
var_dump($g);
var_dump($h);